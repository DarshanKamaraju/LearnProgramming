
public class NestedIfElse {

	public static void main(String[] args) {
		
		        int num = 10;

		        if (num > 0) 
		        {
		            if (num % 2 == 0) 
		            {
		                System.out.println("Number is positive and even.");
		            }
		            else 
		            {
		                System.out.println("Number is positive and odd.");
		            }
		        } 
		        else 
		        {
		            System.out.println("Number is not positive.");
		        }
		    }
		} 
